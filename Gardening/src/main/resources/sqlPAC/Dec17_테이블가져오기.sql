-----------------------------------------------------------------------M1
CREATE TABLE M1  AS
(
SELECT 
	RNUM  COL1
  , 	LPAD('*' , RNUM, '*')   COL2
 FROM 
 	( SELECT ROWNUM RNUM FROM USER_TAB_COLUMNS WHERE ROWNUM < 11 ORDER BY RNUM ASC )
)
;

 
-----------------------------------------------------------------------M2
CREATE TABLE M2 AS 
( 
SELECT 
	RNUM  COL1
	, LPAD('@', RNUM, '@') COL2 
FROM 
	(SELECT 
			DECODE(ROWNUM, 1, 1, 2, 2, 3, 2, 3) RNUM			
		 FROM USER_TAB_COLUMNS WHERE ROWNUM < 6   ORDER BY RNUM ASC)
)
;
