CREATE OR REPLACE PROCEDURE PROC_TST2(N_LIMIT_CNT IN NUMBER )
IS 
	V_CNT NUMBER(3) := 0;
	BEGIN
	  LOOP 
	     INSERT INTO EMP_TST_DIA(EMPNO, ENAME, HIREDATE)
	     VALUES(V_CNT, 'TEST' || TO_CHAR(V_CNT), SYSDATE);
	     V_CNT := V_CNT +1;
	     DBMS_OUTPUT.PUT_LINE(V_CNT);
	     EXIT WHEN V_CNT > N_LIMIT_CNT;
	  END LOOP;
	  COMMIT;
	DBMS_OUTPUT.PUT_LINE(V_CNT || '개의 데이터가 입력되었습니다.');
END 
; 
