SELECT RNUM SEQ
, LPAD('*', RNUM, '*') T1
, LPAD('A',RNUM, 'B') T2
, DECODE(SIGN(RNUM-RVS_PT), 1, (RVS_PT)+1 -(RNUM-(RVS_PT-1)), RNUM) AS DSEQ
, LPAD('*', DECODE(SIGN(RNUM-RVS_PT),1,(RVS_PT)+1-(RNUM-(RVS_PT-1)),RNUM),'*') DFTST
, CASE WHEN RNUM -RVS_PT >= 1 THEN (RVS_PT)+1 -(RNUM-(RVS_PT-1))
   ELSE RNUM
   END AS CSEQ
, LPAD('*', CASE WHEN RNUM -RVS_PT >= 1 THEN (RVS_PT)+1 -(RNUM-(RVS_PT-1))
   ELSE RNUM
   END
   , '*') CFTST
, 'A' || DECODE(RNUM, 1, NULL, F_GET_ALPA('B', RNUM-1)) T4
FROM (
	SELECT ROWNUM RNUM
   , CASE WHEN MOD(ROWNUM,2) = 1 THEN  ROUND((ROWNUM/2),0)  END AS RVS_PT
   , CASE WHEN MOD(ROWNUM,2) = 0 THEN ((ROWNUM/2) + 1) END AS RVS_PT
   FROM ALL_TAB_COLUMNS
	WHERE ROWNUM < 20 
	ORDER BY RNUM ASC
);

RVS_PT ; 
MOD(RNUM,2) 일때 나머지가 1인경우, RVS_PT=ROUND((RNUM /2),0)
MOD(RNUM,2) 일때 나머지가 0인경우,  RVS_PT=RNUM / 2


