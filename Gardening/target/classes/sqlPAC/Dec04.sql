SELECT RNUM SEQ
, LPAD('*', RNUM, '*') T1 
, LPAD('A',RNUM, 'B') T2
, DECODE (SIGN(RNUM-5), 1, 6-((RNUM -4)), RNUM) AS DSEQ
, LPAD('*',DECODE(SIGN(RNUM-5),1,6-((RNUM-4)),RNUM),'*') DFTST
, CASE WHEN RNUM -5 >= 1 THEN ((RNUM -4))  ELSE RNUM  END AS CSEQ
, LPAD('*', CASE WHEN RNUM -5 >= 1 THEN 6-((RNUM -4)) ELSE RNUM END , '*')  CFTST
, 'A' || F_GET_ALPA('B', RNUM) T4
FROM (SELECT ROWNUM RNUM FROM USER_TAB_COLUMNS WHERE ROWNUM < 10 ORDER BY RNUM ASC);

